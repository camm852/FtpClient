    private void ConnectLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConnectLabelMouseClicked
        String ipAddress = IpTextField.getText();
        String port = PortTextField.getText();
        System.out.println(ipAddress);
        System.out.println(port);
        // solicitar conexion
        Socket requestConnection = clientController.connectToServer(port, ipAddress);
        // verificar conexion
        if (requestConnection == null) {
            // Acciones si la conexi贸n no se pudo establecer
            InputDialogUtils.showConnectionErrorDialog();
            // Limpiar cambios de texto
            IpTextField.setText("");
            PortTextField.setText("");
        } else {
            // mensaje si la conexi贸n se estableci贸 correctamente
            InputDialogUtils.showConnectionSuccessDialog();
            // Limpiar cambios de texto
            IpTextField.setText("");
            PortTextField.setText("");
            try {
                Thread.sleep(1500);
            } catch (InterruptedException ex) {
                Logger.getLogger(Connection.class.getName()).log(Level.SEVERE, null, ex);
            }
            // Ocultar la ventana padre del componente actual (ventana Connection)
            setVisible(false);
            // Crear el panel FTP y pasar el socket
            FtpPanel ftpPanel = new FtpPanel(requestConnection, clientController);
            // Mostrar la ventana FtpPanel
            ftpPanel.setVisible(true);
            // Cerrar la ventana Connection
            dispose();
        }
    }//GEN-LAST:event_ConnectLabelMouseClicked

